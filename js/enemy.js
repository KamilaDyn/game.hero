let enemy;

function Enemy(
  enemyType,
  health,
  strength,
  agility,
  hardness,
  superPower,
  speed,
  flying,
  gadgets,
  intelligence
) {
  this.enemyType = enemyType;
  this.health = health;
  this.strength = strength;
  this.agility = agility;
  this.hardness = hardness;
  this.superPower = superPower;
  this.speed = speed;
  this.flying = flying;
  this.gadgets = gadgets;
  this.intelligence = intelligence
}